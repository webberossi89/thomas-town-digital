<p>If you're an established company with strong word-of-mouth, repeat customers, and consistent referral volume, 5–7% is a reasonable floor. If you're trying to grow aggressively, enter a new service area, or compete in a saturated market, push toward 8–10% — at least until you've built the market share you're after.</p>

